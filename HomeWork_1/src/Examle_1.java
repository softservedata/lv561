public class Examle_1 {
    public static void main(String[] args) {
        double Pi = 3.14;
        double r = 10;

        double perymetr = 2 * Pi * r;
        double ploscha = Pi * (r * r);

        System.out.println("Периметр круга = " + perymetr);
        System.out.println("Площа круга = " + ploscha);
    }
}
